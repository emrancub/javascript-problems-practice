// parseFloat
var onlyFloat = parseFloat(3.14);
var stringFloat = parseFloat('3.14');
var stringTextFloat = parseFloat('emran');

// parseInt
var onlyInteger = parseInt(3.14);
var stringInteger = parseInt('3.14');
var stringTextInt = parseInt('emran');

// parseFloat Output
console.log(onlyFloat);
console.log(stringFloat);
console.log(stringTextFloat);

// parseInt Output
console.log(onlyInteger);
console.log(stringInteger);
console.log(stringTextInt);